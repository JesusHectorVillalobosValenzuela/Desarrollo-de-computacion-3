
package soupifyhtml;

public class LibroWcat {
    String titulo;
    String autor;
    String editorial;
    String isbn;
    String oclc;
    String notas;
    String descripcion;

    public LibroWcat(String titulo, String autor, String editorial, String isbn, String oclc, String notas, String descripcion) {
        this.titulo = titulo;
        this.autor = autor;
        this.editorial = editorial;
        this.isbn = isbn;
        this.oclc = oclc;
        this.notas = notas;
        this.descripcion = descripcion;
    }
    
    
}
